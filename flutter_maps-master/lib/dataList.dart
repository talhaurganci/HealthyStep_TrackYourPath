class DataList{
  int index;
  String uId;
  String runName;
  double latitude;
  double longitude;

   DataList(int index, String uId, String runName, double latitude, double longitude) {
   this.index = index;
   this.uId = uId;
   this.runName = runName;
   this.latitude = latitude;
   this.longitude = longitude;
  }
}